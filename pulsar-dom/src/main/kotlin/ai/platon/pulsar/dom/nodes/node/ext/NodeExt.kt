val Node.slimHtml by field {
    val nm = it.nodeName().lowercase()
    when {
        it.isImage || it.isAnchor || it.isNumericLike || it.isMoneyLike || it is TextNode || nm == "li" || nm == "td" -> atomSlimHtml(
